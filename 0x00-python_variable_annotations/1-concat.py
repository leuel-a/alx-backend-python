#!/usr/bin/env python3
"""Defines the concat function"""


def concat(str1: str, str2: str) -> str:
    """Concatenates two strings and returns their result

    Parameters:
    str1 (str): the first string
    str2 (str): the second string

    Returns:
    str: the concatenation of the two strings
    """
    return str1 + str2
